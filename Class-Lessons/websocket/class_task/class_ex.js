function changeColorWhiteRed() {
    if(document.getElementById('whiteRed').style.background === 'white') {
        document.getElementById('whiteRed').style.background = 'red';
    } else {
        document.getElementById('whiteRed').style.background = 'white';
    }
}

function changeColorGreenWhite() {
    if(document.getElementById('greenWhite').style.background === 'green') {
        document.getElementById('greenWhite').style.background = 'white';
    } else {
        document.getElementById('greenWhite').style.background = 'green';
    }
}
